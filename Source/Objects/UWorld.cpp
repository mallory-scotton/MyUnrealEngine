///////////////////////////////////////////////////////////////////////////////
// Dependencies
///////////////////////////////////////////////////////////////////////////////
#include "Objects/UWorld.hpp"

///////////////////////////////////////////////////////////////////////////////
// Namespace UEB
///////////////////////////////////////////////////////////////////////////////
namespace UEB
{

///////////////////////////////////////////////////////////////////////////////
void UWorld::BeginPlay(void)
{
    for (auto& object : mObjects) {
        if (object->IsActive()) {
            object->BeginPlay();
        }
    }
}

///////////////////////////////////////////////////////////////////////////////
void UWorld::Tick(float deltaSeconds)
{
    for (auto& object : mObjects) {
        if (object->IsActive()) {
            object->Tick(deltaSeconds);
        }
    }
}

///////////////////////////////////////////////////////////////////////////////
void UWorld::EndPlay(void)
{
    for (auto& object : mObjects) {
        if (object->IsActive()) {
            object->EndPlay();
        }
    }
}

} // !namespace UEB

///////////////////////////////////////////////////////////////////////////////
/// \brief
///
///////////////////////////////////////////////////////////////////////////////
class BeginPlayNode : public UNode
{
public:
    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    ///////////////////////////////////////////////////////////////////////////
    BeginPlayNode(void)
        : UNode(
            "Event BeginPlay",
            Type::Blueprint,
            ImColor(180, 28, 28),
            NodeIcon::Event
        )
    {
        AddOutputPin(UPin::Type::Flow);
    }

public:
    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \param context
    ///
    ///////////////////////////////////////////////////////////////////////////
    void Evaluate(UEvaluationContext& context) override
    {
        context.template SetPinValue<bool>(mOutputs[0], true);
    }
};

